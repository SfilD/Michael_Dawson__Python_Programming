# Программа выводит список слов в случайном порядке
# На экране печатаются все слова из списка без повторов

import random

WORDS = ["Слово_0", "Слово_1", "Слово_2", "Слово_3", "Слово_4", "Слово_5", "Слово_6", "Слово_7", "Слово_8", "Слово_9"]
for _ in range(len(WORDS)):
    print(WORDS.pop(random.randrange(len(WORDS))))
